is( depth($_->[0]), $_->[1] ) foreach @TESTS;
done_testing();

sub depth {
  ## Split into individual rows....
  ## For each row if
  ##    there are less than 2**$d - 1 entries in the row...
  ## OR there is * * in one of the pairs
  ## OR there is a single * in the last pair.

  my $d = 0;
  for ( split m{\s*\|\s*}, $_[0] ) {
    last if scalar @{[m{\S+}g]} < 2**$d - 1
      || m{^\s*(?:\S+\s+\S+\s+)*?(\*\s+\*|\*\s*$)};
    $d++;
  }
  $d;
}

