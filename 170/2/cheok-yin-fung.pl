#!/usr/bin/env perl
use v5.36;

sub kp {
    my $m1 = $_[0];
    my $m2 = $_[1];
    my $ans = [];
    for my $i ($m1->@*) {
        for my $j ($m2->@*) {
            push $ans->@*, [];
            for my $x ($i->@*) {
                push $ans->[-1]->@*, map {$x*$_} ($j->@*);
            }
        }
    }
    return $ans;
}



use Test::More tests => 8;

is_deeply kp([[13]], [[17]]), [[221]],
    "Scalar test case I";

is_deeply kp( 
     [ [3] ],
     [ [1,2],[3,-1] ]
    ),
     [ [3,6],[9,-3] ],
    "Scalar test case II";

my $v1 = [ [5], [6], [7], [8] ];
my $v2 = [ [1, 2, 3] ];
is_deeply kp($v1, $v2),
     [ [5, 10, 15],
       [6, 12, 18],
       [7, 14, 21],
       [8, 16, 24]],
    "Two vector test case I";

is_deeply kp($v2, $v1),
     [ [5, 10, 15],
       [6, 12, 18],
       [7, 14, 21],
       [8, 16, 24]],
    "Two vector test case II";

is_deeply kp(
     [ [1,2],[3,4] ], 
     [ [5,6],[7,8] ]
    ),

    [ [ 5,  6, 10, 12],
      [ 7,  8, 14, 16],
      [15, 18, 20, 24], 
