use strict;
use warnings;

#------------------------------------------------------------------------------
BEGIN
#------------------------------------------------------------------------------
{
    #$| = 1;
    #print "\nChallenge 104, Task #1: FUSC Sequence (Perl)\n\n";
}

#==============================================================================
MAIN:
#==============================================================================
{
    my @fusc = (0, 1);

    for my $n (1 .. 25)
    {
        my $fusc_n = $fusc[ $n ];

        $fusc[ 2 * $n     ] = $fusc_n;
        $fusc[ 2 * $n + 1 ] = $fusc_n + $fusc[ $n + 1 ];
    }

    printf "fusc(%2d) = %2d\t", $_, $fusc[ $_ ] for 0 .. $#fusc - 1;
    print  "\n";
}

