#!/usr/bin/env perl
use v5.36;

my $N = 1122234;
die "Wrong entry" unless ( $N =~ /\b\d+\b/ ) ;
my $count = 1 ;
my $pos = 0 ;
my $output ;
my $num = substr( $N, $pos , 1 ) ;
my $lastValue = $num ;
$pos = 1 ;
while ( $pos < length $N ) {
  $num = substr( $N , $pos , 1 ) ;
  if ( $num eq $lastValue ) {
      $count++ ;
  }
  else {
      $output .= ( $count . $lastValue ) ;
      $lastValue = $num ;
      $count = 1 ;
  }
  $pos++ ;
}
if ( $count == 1 ) {
  $output .= ( $count . $lastValue ) ;
}
print "$output\n" ;
